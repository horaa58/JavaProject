    private void btnGenerateReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateReportActionPerformed
       ParkingEntryFileHandler fileHandler = new ParkingEntryFileHandler();

       ArrayList<ParkingEntry> entryList = fileHandler.loadEntries();

       int currentlyParked = 0;
       int unpaid = 0;

       for (ParkingEntry entry : entryList) {

          if (entry.getParkingStatus().equalsIgnoreCase("PARKED")) {
          currentlyParked++;
        }

          if (entry.getPaymentStatus().equalsIgnoreCase("UNPAID")) {
          unpaid++;
        }
}

JOptionPane.showMessageDialog(
        this,"Security Monitoring Report\n\n"+ "Currently Parked Vehicles: " + currentlyParked + "\n"+ "Vehicles with Unpaid Parking Fee: " + unpaid
);        
    }//GEN-LAST:event_btnGenerateReportActionPerformed

