    private void loadTable() {

    ParkingEntryFileHandler fileHandler = new ParkingEntryFileHandler();

    ArrayList<ParkingEntry> entryList = fileHandler.loadEntries();

    DefaultTableModel model = (DefaultTableModel) tblEntries.getModel();

    model.setRowCount(0);

    for (ParkingEntry entry : entryList) {

        model.addRow(new Object[]{
            entry.getEntryId(),
            entry.getVehicleNumber(),
            entry.getOwnerName(),
            entry.getSlotId(),
            entry.getEntryDate(),
            entry.getEntryTime(),
            entry.getExitDate(),
            entry.getExitTime(),
            entry.getParkingStatus(),
            entry.getPaymentStatus()
        });
    }
}

