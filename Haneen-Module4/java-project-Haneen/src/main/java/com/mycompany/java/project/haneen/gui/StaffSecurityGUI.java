    private void btnSearchVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchVehicleActionPerformed
       String vehicleNumber = JOptionPane.showInputDialog(this,"Enter Vehicle Number:");
       ParkingEntryFileHandler fileHandler = new ParkingEntryFileHandler();

       ArrayList<ParkingEntry> entryList = fileHandler.loadEntries();

       DefaultTableModel model = (DefaultTableModel) tblEntries.getModel();

       model.setRowCount(0);
       
       boolean found = false;

       for (ParkingEntry entry : entryList) {

          if (entry.getVehicleNumber().equalsIgnoreCase(vehicleNumber)) {

             model.addRow(new Object[]{
             entry.getEntryId(),
             entry.getVehicleNumber(),
             entry.getOwnerName(),
             entry.getSlotId(),
             entry.getEntryDate(),
             entry.getEntryTime(),
             entry.getExitDate(),
             entry.getExitTime(),
             entry.getParkingStatus(),
             entry.getPaymentStatus()
        });

        found = true;
    }
}
     if (!found) {
    JOptionPane.showMessageDialog(this, "Vehicle not found.");
}  
       
    }//GEN-LAST:event_btnSearchVehicleActionPerformed

