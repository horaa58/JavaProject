    private void btnDeleteStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteStaffActionPerformed
       int selectedRow = tblStaff.getSelectedRow();

       if (selectedRow == -1) {
           JOptionPane.showMessageDialog(this,"Please select a staff record to delete.");
              return;
    }

       String staffId = tblStaff.getValueAt(selectedRow, 0).toString();

       StaffFileHandler fileHandler = new StaffFileHandler();

       ArrayList<ParkingStaff> staffList = fileHandler.loadStaff();

       boolean deleted = false;

       for (ParkingStaff staff : staffList) {

        if (staff.getStaffId().equalsIgnoreCase(staffId)) {
            staffList.remove(staff);
            deleted = true;
            break;
        }
    }

        if (deleted) {
           fileHandler.saveStaff(staffList);
           loadStaffTable();

        JOptionPane.showMessageDialog(this,"Staff deleted successfully.");
    }        
    }//GEN-LAST:event_btnDeleteStaffActionPerformed

    private void btnAddStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStaffActionPerformed
       String staffId = txtStaffId.getText();
       String staffName = txtStaffName.getText();

       String role = cmbRole.getSelectedItem().toString();
       String assignedGate = cmbAssignedGate.getSelectedItem().toString();
       String assignedFloor = cmbAssignedFloor.getSelectedItem().toString();

       String shiftTime = txtShiftTime.getText();

       String staffStatus = cmbStaffStatus.getSelectedItem().toString();

       if (staffId.trim().isEmpty() || staffName.trim().isEmpty() || shiftTime.trim().isEmpty()) {

           JOptionPane.showMessageDialog(this,"Please fill in all required fields.");

           return;
    }

    StaffFileHandler fileHandler = new StaffFileHandler();

    ArrayList<ParkingStaff> staffList = fileHandler.loadStaff();

    for (ParkingStaff existingStaff : staffList) {

        if (existingStaff.getStaffId().equalsIgnoreCase(staffId)) {

            JOptionPane.showMessageDialog(this,"Staff ID already exists.");

            return;
        }
    }

    ParkingStaff staff = new ParkingStaff(
            staffId,
            staffName,
            role,
            assignedGate,
            assignedFloor,
            shiftTime,
            staffStatus
    );

    staffList.add(staff);

    fileHandler.saveStaff(staffList);

    loadStaffTable();

    txtStaffId.setText("");
    txtStaffName.setText("");
    cmbRole.setSelectedIndex(0);
    cmbAssignedGate.setSelectedIndex(0);
    cmbAssignedFloor.setSelectedIndex(0);
    txtShiftTime.setText("");
    cmbStaffStatus.setSelectedIndex(0);

    JOptionPane.showMessageDialog(this,"Staff added successfully.");
       
    }//GEN-LAST:event_btnAddStaffActionPerformed

