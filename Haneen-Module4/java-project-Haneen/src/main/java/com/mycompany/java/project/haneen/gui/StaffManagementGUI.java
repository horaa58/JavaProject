    private void btnSearchStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchStaffActionPerformed
       String searchBy = cmbSearchBy.getSelectedItem().toString();
       String searchValue = txtSearchValue.getText().trim();

       if (searchValue.isEmpty()) {
           JOptionPane.showMessageDialog(this,"Please enter a search value.");
        return;
    }

       StaffFileHandler fileHandler = new StaffFileHandler();
       ArrayList<ParkingStaff> staffList = fileHandler.loadStaff();

       DefaultTableModel model =
            (DefaultTableModel) tblStaff.getModel();

       model.setRowCount(0);

       boolean found = false;

       for (ParkingStaff staff : staffList) {

          boolean match = false;

           if (searchBy.equals("Staff ID")) {
              match = staff.getStaffId().equalsIgnoreCase(searchValue);

        } 
           else if (searchBy.equals("Staff Name")) {
            match = staff.getStaffName().equalsIgnoreCase(searchValue);

        }
           else if (searchBy.equals("Role")) {
            match = staff.getRole().equalsIgnoreCase(searchValue);

        }
           else if (searchBy.equals("Assigned Gate")) {
            match = staff.getAssignedGate().equalsIgnoreCase(searchValue);

        }
           else if (searchBy.equals("Assigned Floor")) {
            match = staff.getAssignedFloor().equalsIgnoreCase(searchValue);

        }
           else if (searchBy.equals("Staff Status")) {
            match = staff.getStaffStatus().equalsIgnoreCase(searchValue);
        }

        if (match) {

            model.addRow(new Object[]{
                staff.getStaffId(),
                staff.getStaffName(),
                staff.getRole(),
                staff.getAssignedGate(),
                staff.getAssignedFloor(),
                staff.getShiftTime(),
                staff.getStaffStatus()
                    
            });

            found = true;
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(this, "No staff record found.");
    }        
    
    }//GEN-LAST:event_btnSearchStaffActionPerformed

