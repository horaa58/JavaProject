    private void btnUpdateStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStaffActionPerformed
       int selectedRow = tblStaff.getSelectedRow();

       if (selectedRow == -1) {
          JOptionPane.showMessageDialog(this,"Please select a staff record to update.");
           return;
}

       String staffId = txtStaffId.getText().trim();
       String staffName = txtStaffName.getText().trim();
       String role = cmbRole.getSelectedItem().toString();
       String assignedGate = cmbAssignedGate.getSelectedItem().toString();
       String assignedFloor = cmbAssignedFloor.getSelectedItem().toString();
       String shiftTime = txtShiftTime.getText().trim();
       String staffStatus = cmbStaffStatus.getSelectedItem().toString();

       if (staffId.isEmpty() || staffName.isEmpty() || shiftTime.isEmpty()) {
          JOptionPane.showMessageDialog(this,"Please fill in all required fields.");
           return;
}

     StaffFileHandler fileHandler = new StaffFileHandler();
     ArrayList<ParkingStaff> staffList = fileHandler.loadStaff();

     for (ParkingStaff staff : staffList) {

     if (staff.getStaffId().equalsIgnoreCase(staffId)) {

        staff.setStaffName(staffName);
        staff.setRole(role);
        staff.setAssignedGate(assignedGate);
        staff.setAssignedFloor(assignedFloor);
        staff.setShiftTime(shiftTime);
        staff.setStaffStatus(staffStatus);

        break;
    }
}

     fileHandler.saveStaff(staffList);

     loadStaffTable();

     txtStaffId.setText("");
     txtStaffName.setText("");
     cmbRole.setSelectedIndex(0);
     cmbAssignedGate.setSelectedIndex(0);
     cmbAssignedFloor.setSelectedIndex(0);
     txtShiftTime.setText("");
     cmbStaffStatus.setSelectedIndex(0);

     JOptionPane.showMessageDialog(this,"Staff updated successfully.");        
    }//GEN-LAST:event_btnUpdateStaffActionPerformed

